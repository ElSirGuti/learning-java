// Ejemplo: Realizar un programa que imprima la siguiente serie numeríca: 1, 3, 5, 7, 9,

public class dia3c3{
    public static void main(String args[]){
        int i = 1;

        while(i<10){
            System.out.print(i + ", ");
            i+=2;
        }

    }
}
